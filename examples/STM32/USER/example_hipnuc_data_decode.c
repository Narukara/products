static void parse_example_data(const char* name, const uint8_t* data, size_t size)
{
    int i;
    static hipnuc_raw_t hipnuc_raw = {0};
    static char log_buf[512];
    uint8_t decode_succ = 0;

    printf("Parsing %s data:\n", name);
    for (i = 0; i < size; i++) 
    {
        decode_succ = hipnuc_input(&hipnuc_raw, data[i]);
        if (decode_succ) 
        {
            hipnuc_dump_packet(&hipnuc_raw, log_buf, sizeof(log_buf));
            printf("Decoded frame length: %d\n", hipnuc_raw.len);
            printf("%s\n", log_buf);
        }
    }
    printf("\n");
}


